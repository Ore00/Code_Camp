function dropElements(arr, func) {
  // Drop them elements.
  for(var x = 0; x < arr.length-1; x++){
      if(func(arr[x]) === true){           
         break;
      }
  } 
  if(x === arr.length-1){
     arr = []; 
  }else{
      arr = arr.splice(x);
  }
 
   return arr;
}

console.log(dropElements([1, 2, 3, 4], function(n) {return n > 5; }));

